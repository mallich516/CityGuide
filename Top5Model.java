package com.example.loginpage.Top5cities;

public class Top5Model {

    String image;
    String title;

    public Top5Model() {
        this.image = image;
        this.title = title;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
